public class JavaDataTypes {
    public static void main(String[] args) {
        byte b = 6;
        System.out.println(b);
        char ch = 'c';
        System.out.println(ch);
        boolean var = false;
        System.out.println(var);
        double pi = 3.14;
        System.out.println(pi);
        int number = 89;
        System.out.println(number);
        // float
        // long
        short num = 240;
        System.out.println(num);
    }
}
