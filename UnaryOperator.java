public class UnaryOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        int c = 6;
        int d = c++;
        System.out.println(c);
        System.out.println(d);

        int num = 45;
        num++;
        System.out.println(num);
    }
}
